function fizzbuzz($num1,$num2){
for ($x=$num1 ; $x<=$num2 ; $x++ ){
    if ($x%3 ==0 && $x%5 ==0  ){
        print "fizzbuzz ";
    }else if ($x%3 ==0  ){
        print "fizz ";
    }else if ($x%5 ==0  ){
        print "Buzz ";
    }else {
        print ($x) . " " ;
    };
    };
    print "<br><br>";
};

